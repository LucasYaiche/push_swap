#include "push_swap.h"

void	up(int *tab)
{
	int	temp;
	int	last;
	int	len;

	len = 0;
	while (tab[len + 1])
	{
		tab[len] = tab[len + 1];
		len++;
	}
	tab[len] = '\0';
}
